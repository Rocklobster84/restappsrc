const mongoose = require('mongoose');

const meshlinkSchema = new mongoose.Schema({
	title: {
		type: String,
	},
	slug: {
		type: String,
	},
	description: {
		type: String,
	},
	copy: {
		type: String,
	},
});

module.exports = mongoose.model('MeshLink', meshlinkSchema, 'MeshLink');
